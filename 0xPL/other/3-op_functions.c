/**
 * op_add - sum two integers
 * @a: value
 * @b: value
 * Return: integer
 */

int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - substract two integers
 * @a: value
 * @b: value
 * Return: integer
 */

int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: value
 * @b: value
 * Return: integer
 */

int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: value
 * @b: value
 * Return: integer
 */

int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - module of a number
 * @a: value
 * @b: value
 * Return: integer
 */
int op_mod(int a, int b)
{
    return (a % b);
}